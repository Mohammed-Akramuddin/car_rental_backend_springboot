package com.car_rental_backend.enums;

/**
 * Enumeration of luxury car categories.
 * Used for filtering and pricing differentiation.
 */
public enum CarCategory {
    SUV,        // Sport Utility Vehicles (e.g., Range Rover, Porsche Cayenne)
    SEDAN,      // Executive sedans (e.g., Mercedes S-Class, BMW 7 Series)
    LUXURY,     // Ultra-luxury vehicles (e.g., Rolls-Royce, Bentley)
    SPORTS,     // Sports cars (e.g., Ferrari, Lamborghini)
    CONVERTIBLE // Convertible luxury cars (e.g., Porsche 911 Cabriolet)
}
